using System;

namespace MyFirstProgram
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Hello\a sir!"); //bell alert
            Console.WriteLine("Hello\b sir!"); //backspace
            Console.WriteLine("Hello\f sir!"); //from feed
            Console.WriteLine("Hello\n sir!"); //new line
            Console.WriteLine("Hello\r sir!"); //Carriage return
            Console.WriteLine("Hello\t sir!"); //horizontal tab
            Console.WriteLine("Hello\v sir!"); //Verticaltab
            Console.WriteLine("Hello\' sir!"); //Single quatation mark
            Console.WriteLine("Hello\" sir!"); //Double quatation mark
            Console.WriteLine("Hello\\ sir!"); //Backlash
            
        }


    }
}
